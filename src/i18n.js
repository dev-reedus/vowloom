export const LANG_KEY = 'nozze.lang.v1'

export function loadLang() {
  try {
    const saved = localStorage.getItem(LANG_KEY)
    if (saved === 'it' || saved === 'en') return saved
  } catch {
    /* ignore */
  }
  return 'it'
}
